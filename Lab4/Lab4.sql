FROM AP.INVOICES
WHERE ROWNUM <= 10;

--2 
SELECT TO_CHAR(INVOICE_TOTAL, '9.99EEEE') AS "Scientific Notation"
FROM AP.INVOICES
WHERE ROWNUM < = 10;

--3
SELECT TO_CHAR(INVOICE_DATE, 'MM/DD/YYYY') AS "Formatted Invoice Date"
FROM AP.INVOICES
WHERE ROWNUM <= 10;

