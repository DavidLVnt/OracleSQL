--6
SELECT CONCAT(CITY, CONCAT(', ' ,CONCAT(STATE, CONCAT (' ', ZIP)))) AS "All Together with Concat"  
FROM STUDENT.ZIPCODE 
WHERE ROWNUM <= 10;

--7
SELECT CITY || ', ' || STATE || ', ' ||  ZIP AS "All Together with ||"
FROM STUDENT.ZIPCODE
WHERE ROWNUM <= 10;

--8
SELECT CITY, UPPER(CITY) AS "Upper", LOWER(CITY) AS "Lower", INITCAP(CITY) AS "Mixed"
FROM STUDENT.ZIPCODE
WHERE ROWNUM <= 10;

--9
SELECT SUBSTR(CITY,1,9)
FROM STUDENT.ZIPCODE
WHERE ROWNUM <= 10
ORDER BY CITY ;

--10
SELECT CITY, SUBSTR(CITY,2,3) AS "Two through Four"
FROM STUDENT.ZIPCODE
WHERE ROWNUM <= 10
ORDER BY CITY ;

--11
SELECT CITY, SUBSTR(CITY,-3,3) AS "Last Three"
FROM STUDENT.ZIPCODE
WHERE ROWNUM <= 10 
ORDER BY CITY ;

